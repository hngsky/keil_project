#include "rs485.h"



void RS485_COM1_SetTX(void)
{
    HAL_GPIO_WritePin(RS485_COM1_DIR_GPIO_PORT, RS485_COM1_DIR_PIN, GPIO_PIN_SET);
}

void RS485_COM1_SetRX(void)
{
    HAL_GPIO_WritePin(RS485_COM1_DIR_GPIO_PORT, RS485_COM1_DIR_PIN, GPIO_PIN_RESET);
}

